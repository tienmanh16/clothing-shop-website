package model;

/**
 *
 * @author hii
 */
public class ShoppingCart {
    private int cartID, userID;

    public ShoppingCart() {
    }

    public ShoppingCart(int cartID, int userID) {
        this.cartID = cartID;
        this.userID = userID;
    }

    public int getCartID() {
        return cartID;
    }

    public void setCartID(int cartID) {
        this.cartID = cartID;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }
    
}
